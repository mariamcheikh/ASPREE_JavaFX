package edu.esprit.pidev.models;

import java.util.Objects;


public class Df_category {
    private int id;
    private String name;
public Df_category()
{
}

    public Df_category(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Df_category(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 71 * hash + this.id;
        hash = 71 * hash + Objects.hashCode(this.name);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Df_category other = (Df_category) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Df_category{" + "id=" + id + ", name=" + name + '}';
    }
  
}

